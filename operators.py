x=10
y=5
print("Arithmetic operators")
print("Addition",x+y)
print("Subtraction",x-y)
print("Multiplication",x*y)
print("Division",x%y)
print("Modular division",x+y)
print("Assignment operators")
x+=3
print("+=",x)
x-=3
print("-=",x)
x*=3
print("*=",x)
x/=3
print("/=",x)
x%=3
print("%=",x)
print("Logical operators")
print("AND",x>5 and x<15)
print("OR",x>5 or x<4)
print("NOT",not(x>11 and x<5))
print("Bitwise operators")
x=10
y=5
print("&",x&y)
print("|",x|y)
print("^",x^y)
print("<<",x<<2)
print(">>",x>>2)
x=10
y=5
print("Comparison operators")
print("==",x==y)
print("!=",x!=y)
print(">",x>y)
print("<",x<y)
print(">=",x>=y)
print("<=",x<=y)
print("Identity operators")
x=5
y=6
print("is",x is y)
print("is not",x is not y)
print("Membership operators")
x=[1,2,3]
print("in",1 in x)
print("not in",2 not in x)